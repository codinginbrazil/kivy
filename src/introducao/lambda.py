# Lambda é uma função anônima composta apenas por expressões.
#
# As funções lambda podem ter apenas uma linha, e podem ser atribuídas a uma variável. 
#
# Funções lambda são muito usadas em programação funcional.
#
# Funções lambda consomem menos recursos computacionais que as funções convencionais,
# porém são mais limitados.
#
# Sintaxe:
#   lambda <lista de variáveis>: <expressões >
